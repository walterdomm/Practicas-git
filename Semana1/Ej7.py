# Escribe un programa que calcule el área de un triángulo a partir de la base y la
# altura dadas.

print('Calculando el área de un triángulo')
base = input('Ingrese la base del triangulo aquí')
altura = input('Y aquí ingrese la altura del mismo ')
area = (int(base)*int(altura)) / 2
print(f'El área del triángulo es: {area}')